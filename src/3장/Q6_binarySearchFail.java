import java.util.Arrays;
import java.util.Scanner;

public class Q6_binarySearchFail {

	public static void main(String[] args) {
		Scanner stdIn = new Scanner(System.in);

		System.out.print("요솟수 : ");
		int num = stdIn.nextInt();
		int[] x = new int[num];				// 요솟수가 num 인 배열 

		System.out.println("오름차순으로 입력하세요.");

		System.out.print("x[0] : ");		// 맨앞 요소를 입력받음
		x[0] = stdIn.nextInt();

		for (int i = 1; i < num; i++) {
			do {
				System.out.print("x[" + i + "] : ");
				x[i] = stdIn.nextInt();
			} while (x[i] < x[i - 1]);	// 바로 앞의 요소보다 작으면 다시 입력
		}

		System.out.print("검색 값 : ");	// 키값을 입력받음
		int ky = stdIn.nextInt();

		int idx = Arrays.binarySearch(x, ky); //java.util.Arrays의 binarySearch(int[], int)메소드 사용
		//자료형에 따른 메소드 호출은 컴파일러가 자동으로 결정한다.

		if (idx < 0) {
			int insertP = -idx - 1;
            System.out.println("검색 값의 요소가 없습니다.");
			System.out.printf("삽입 포인트: %d\n", insertP);
		} else
			System.out.println("그 값은 x[" + idx + "]에 있습니다.");
	}
}